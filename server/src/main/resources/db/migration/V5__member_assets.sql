CREATE TABLE member_point_accounts (
  user_id BIGINT PRIMARY KEY,
  balance INT NOT NULL DEFAULT 0,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_member_point_accounts_user FOREIGN KEY (user_id) REFERENCES users(id),
  CONSTRAINT chk_member_point_balance CHECK (balance >= 0)
);

CREATE TABLE member_point_transactions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL,
  type VARCHAR(32) NOT NULL,
  delta_points INT NOT NULL,
  balance_after INT NOT NULL,
  source VARCHAR(32) NOT NULL,
  source_order_id BIGINT,
  reason VARCHAR(255),
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_member_point_transactions_user FOREIGN KEY (user_id) REFERENCES users(id),
  CONSTRAINT fk_member_point_transactions_order FOREIGN KEY (source_order_id) REFERENCES orders(id),
  CONSTRAINT chk_member_point_delta CHECK (delta_points <> 0),
  CONSTRAINT chk_member_point_after CHECK (balance_after >= 0)
);

CREATE INDEX idx_member_point_transactions_user ON member_point_transactions(user_id, created_at);
CREATE UNIQUE INDEX uk_member_point_order_type ON member_point_transactions(source_order_id, type);

CREATE TABLE member_wallet_accounts (
  user_id BIGINT PRIMARY KEY,
